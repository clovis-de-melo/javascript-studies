// Function 

// input: data that we can send to the function (as long as we want to)
// process
// output: result or return 

function addition(a, b) { 
    let result = a + b;
    return result 
}

let x = addition(15, 5);
let y = addition(2, 8);

console.log(x);
console.log(y);

// Arrow Function 

let subtraction = (a, b) => {
    let result = a - b 
    return result
} 

let z = subtraction(50, 20);
console.log(z);

