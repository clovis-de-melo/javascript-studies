// Comparison Operators

// == equal to 

x = 5 

console.log(x == 2) // false
console.log(2 == 2) // true
console.log(x == "5") // true

// === equal value and equal type

console.log(x === 5) // true
console.log(x === "5") // false

// != not equal

console.log(x != 5) // false
console.log(x != 3) // true
console.log(x != "5") // false

// !== not equal value or not equal type
